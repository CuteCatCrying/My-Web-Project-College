    <h3>Necroplanetologi, Bidang Studi Astronomi yang Baru Diketahui</h3>
    <br />
    <p>
        Nationalgeographic.co.id - Pada 2015, para astronom menemukan bintang
        katai putih yang berjarak 570 tahun cahaya dari Bumi dan memiliki pola
        redup yang aneh. Bintang tersebut meredup beberapa kali di beberapa
        titik kedalamannya. Atmosfernya juga dipenuhi dengan unsur-unsur yang
        semestinya ditemukan di planet berbatu.
    </p>
    <br />
    <p>
        Bintang katai putih tersebut adalah WD 1145 + 017 yang gravitasinya mati
        tak lama dari penemuannya yang berada dalam proses menghancurkan
        benda-benda antariksa di sekitar orbitnya. Meskipun umumnya bintang
        katai putih umumnya mengeluarkan banyak material ketika mati dengan
        suatu ledakan, tapi anehnya pada peristiwa besar tersebut, benda
        antariksa sekitarnya bisa selamat.
    </p>
    <br />
    <p>
        Bangkai WD 1145 + 017 pun menjadi rekonstruksi forensik astronomi untuk
        memahami bagaimana dan seperti apa caranya suatu benda bintang mati.
    </p>
    <br />
    <p>
        Pada fenomena seperti inilah, para astronom Inggris dan Amerika Serikat
        menyebutnya sebagai necroplanetologi, bidang baru astronomi untuk
        mempelajari bangkai antariksa.
    </p>
